		<h2>Qu'est ce que le staking ?</h2>

		<p>Le staking est un mécanisme de sécurisation de la Blockchain. Pour que le réseau traite correctement les transactions effectuées, les utilisateurs doivent déposer leurs cryptomonnaies dans un coffre numérique. Ce coffre est relié à un code informatique qui se charge de récupérer et traiter toutes les transactions qui ont lieu sur la Blockchain. En échange de cette action, les utilisateurs qui se chargent de sécuriser le réseau toucheront des récompenses pour chaque bloc crée.<br><br>

		Le fait de bloquer ses cryptomonnaies dans le coffre permet de montrer que les déposeurs, et donc par implication, le smart contract qui gère le code informatique, est bienveillant et agit de la sorte. La Blockchain peut donc se reposer dessus et ainsi continuer son processus de traitement. <br><br>

		Il est à noter toutefois qu'un smart contract peut mal fonctionner, de manière volontaire ou non, et ainsi envoyer de mauvaises informations à la Blockchain. Dans ce cas, les autres noeuds du réseau vont détecter cette malversation et avertir la Blockchain. Si les craintes sont vérifiées, les cryptomonnaies bloquées dans le smart contract seront récupérés au détriment des déposeurs en guise de punition. Il est donc primordial de déposer ses jetons sur un noeud sûr qui a déjà fait ses preuves, au risque de perdre ses fonds.

		</p>

