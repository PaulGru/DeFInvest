		<h2>Le système lending/borrowing</h2>

		<p>Il existe une particularité propre à l’emprunt de cryptomonnaies. Dans la finance traditionnelle, lorsque vous demandez un prêt à une banque et qu’elle vous l’accorde, à partir du moment où vous recevez la somme, il faudra la rembourser mensuellement avec des intérêts, au risque de voir la banque récupérer une partie de vos biens pour se rembourser. <br><br>

		Mais dans le monde de la Blockchain, toutes les transactions sont anonymisées. Ainsi, si vous empruntez des cryptomonnaies et que vous décidez de ne pas rembourser un certain montant mensuel, aucune institution financière ne pourra venir faire pression. Dans ce cas, il vous sera demandé de déposer en amont une certaine somme de cryptomonnaies appelée collatéral pour pouvoir emprunter. Lorsque vous allez emprunter des cryptomonnaies, vous allez dans un premier temps en déposer sur la plateforme pour collatéraliser votre position. La valeur du montant de cryptomonnaies déposées devra toujours être supérieure à la valeur du montant de cryptomonnaies empruntées pour rendre incitatif le remboursement du prêt. On parle dans ce cas de surcollatéralisation : le ratio (valeur déposée) / (valeur empruntée) > 1. cela permet au protocole de pouvoir se rembourser sur les fonds que vous avez déposé si jamais vous décidez de ne pas rembourser votre prêt.<br><br>

		<h2>Les services de DeFInvest</h2>

		<p>DeFInvest se charge de déposer vos fonds sur un smart contract sécurisé afin de les prêter et d'en retirer des rendements. A savoir que les fonds prêtés seront considérés comme colatéral ce qui nous permettra de pouvoir emprunter par la suite. L'objectif est le suivant : emprunter à des taux plus faibles que les prêts. En ce sens, nous réalisons un arbitrage, chercher les taux emprunteurs les plus faibles et les prêteurs les plus élevés afin d'en tirer un profit le plus élevé possible. <br><br>

		En d'autres termes, DeFInvest réalise des boucles : les crypto-actifs prêtés servent de collatéral pour pouvoir emprunter et ce nouvel emprunt peut être de nouveau prêter en tant que collatéral pour pouvoir perpétuer la boucle.</p>

