		<p>Ce produit utilise deux outils : les pools de liquidité (liquidity providing) et le lending.</p>

		<h3>Pool de liquidité (liquidity providing)</h3>

		<p>
			Concrètement un pool de liquidité regroupe une paire de cryptomonnaies, par exemple ETH et BTC. Le ratio sera toujours de 50-50, c’est-à-dire que 50% de la valeur du pool est en BTC et 50% en ETH. Si vous possédez de l’ETH ou du BTC ou les deux, vous pouvez les déposer dans le pool de liquidité, ce qui vous permettra d’obtenir des rendements en tant qu’apporteur de liquidité. <br>

			Les pools de liquidités permettent aux traders der réaliser des arbitrages et ainsi gagner de l’argent. Par exemple, si le prix de l’ETH est à 3000 dollars sur Binance et à 2500 dollars dans le pool, le trader va échanger ses BTC contre des ETH dans le pool pour par la suite les vendre sur Binance à un prix plus haut qu’auquel il les a achetés.
		</p>

		<h3>D'où proviennent les rendements ?</h3>

		<p>
			D’abord, à chaque fois qu’un trader va effectuer un arbitrage, il va devoir payer des frais de transactions, qui seront perçus par les fournisseurs de liquidités.<br>

			Aussi, la concurrence est rude entre les plateformes gérant les pools de liquidités pour attirer le maximum de liquidités, donc elles incitent les apporteurs de liquidités en les rémunérants pour avoir utilisé leurs services. Cette rémunération se fait via le jeton de gouvernance de la plateforme, et non pas dans la cryptomonnaies que vous avez déposées. DeFInvest se charge alors d'échanger ces jetons contre des stablecoins. <br>

			Pour le produit Livret, les fonds utilisées ne seront pas des cryptomonnaies mais des stablecoins, c'est à dire des cryptoactifs dont la valeur est équivalente à un dollar. Cela permet ainsi de ne pas être impacté par l'impermanent loss, c'est à dire la perte due à la variation des crypto-actifs présents dans le pool de liquidité.
		</p>

		<p>
			En plus de liquidity providing, le produit Livret utilise le lending. A noter toutefois que contrairement au produit Lending, le produit Livret ne s'occupe que du lendind de stablecoin pour rester dans la philosophie initiale : permettre d'obtenir des rendements sans être affecté par la variation des prix du marché des cryptomonnaies.
		</p>

